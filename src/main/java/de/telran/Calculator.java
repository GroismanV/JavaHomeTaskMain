package de.telran;

public class Calculator {

    public double add(double x, double y) {
        double result = x + y;
        return result;
    }

    public double sub(double x, double y) {
        double result = x - y;
        return result;
    }

    public double multiplication(double x, double y) {
        double result = x * y;
        return result;
    }

    public double div(double x, double y) {
        double result = x / y;
        return result;
    }
}
